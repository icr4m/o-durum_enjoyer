#include "minishell.h"

void	sigint_handler(int signum)
{
	(void)signum;
	g_signal_received = 128 + signum;
	write(1, "\n", 1);
	rl_on_new_line();
	rl_replace_line("", 0);
	rl_redisplay();
}

void	handle_signal_parent(int num)
{
	g_signal_received = num + 128;
}
